using System;


namespace _9.CharacterType
{
    class CharacterType
    {
        static void Main()
        {
            char symbol = 'а';
            Console.WriteLine("The code of  '{0}' is:{1}", symbol, (int) symbol);
            char symbol = 'b';
            Console.WriteLine("The code of  '{0}' is:{1}", symbol, (int) symbol);
            char symbol = 'A';
            Console.WriteLine("The code of  '{0}' is:{1}", symbol, (int) symbol);


        }
    }
}
